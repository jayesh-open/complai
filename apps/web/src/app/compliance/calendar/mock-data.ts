export function getComplianceEvents(today: Date = new Date()): (ComplianceEvent & { dueDate: Date })[] {
  return EVENTS_TEMPLATE.map((tmpl, i) => ({
    ...tmpl,
    id: `evt-${String(i + 1).padStart(3, "0")}`,
    dueDate: addDays(today, tmpl.dueDateOffset),
  }));
}

export function resolveEventsForMonth(
  events: (ComplianceEvent & { dueDate: Date })[],
  year: number,
  month: number,
): (ComplianceEvent & { dueDate: Date })[] {
  return events.filter((e) => e.dueDate.getFullYear() === year && e.dueDate.getMonth() === month);
}
