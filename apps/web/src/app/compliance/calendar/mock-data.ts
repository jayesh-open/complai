import type { ComplianceEvent } from "./types";

function addDays(base: Date, days: number): Date {
  const d = new Date(base);
  d.setDate(d.getDate() + days);
  return d;
}

function monthLabel(base: Date, offset: number): string {
  const d = addDays(base, offset);
  return d.toLocaleString("en-IN", { month: "long" });
}

function yearLabel(base: Date, offset: number): string {
  return addDays(base, offset).getFullYear().toString();
}

function fy(base: Date, offset: number): string {
  const d = addDays(base, offset);
  const y = d.getMonth() >= 3 ? d.getFullYear() : d.getFullYear() - 1;
  return `${y}-${String(y + 1).slice(2)}`;
}

